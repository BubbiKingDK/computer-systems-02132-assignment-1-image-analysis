void draw_x(unsigned char input_image[BMP_WIDTH][BMP_HEIGTH][BMP_CHANNELS])
{
  for (int i = 0; i < coordinates_count; i++)
  {
    int cx = coordinates[i].x;
    int cy = coordinates[i].y;

    for (int y_offset = 0; y_offset < ARRAY_HEIGHT; y_offset++)
    {
      for (int x_offset = 0; x_offset < ARRAY_WIDTH; x_offset++)
      {

        if (draw_array[y_offset][x_offset] != 1)
        {
          int x = cx + (x_offset - ARRAY_WIDTH / 2);
          int y = cy + (y_offset - ARRAY_HEIGHT / 2);

          if (x >= 0 && x < BMP_WIDTH && y >= 0 && y < BMP_HEIGTH)
          {
            switch (draw_array[y_offset][x_offset])
            {
            case 0:
              input_image[x][y][0] = Base_color.r;
              input_image[x][y][1] = Base_color.g;
              input_image[x][y][2] = Base_color.b;
              break;

            case 2:
              input_image[x][y][0] = Green_color.r;
              input_image[x][y][1] = Green_color.g;
              input_image[x][y][2] = Green_color.b;
              break;

            case 4:
              input_image[x][y][0] = White_color.r;
              input_image[x][y][1] = White_color.g;
              input_image[x][y][2] = White_color.b;
              break;

            case 5:
              input_image[x][y][0] = Orange_color.r;
              input_image[x][y][1] = Orange_color.g;
              input_image[x][y][2] = Orange_color.b;
              break;

            case 6:
              input_image[x][y][0] = Cig_color.r;
              input_image[x][y][1] = Cig_color.g;
              input_image[x][y][2] = Cig_color.b;
              break;

            case 7:
              input_image[x][y][0] = Glod_color.r;
              input_image[x][y][1] = Glod_color.g;
              input_image[x][y][2] = Glod_color.b;
              break;

            case 8:
              input_image[x][y][0] = Ash_color.r;
              input_image[x][y][1] = Ash_color.g;
              input_image[x][y][2] = Ash_color.b;
              break;

            case 9:
              input_image[x][y][0] = Mcd_color.r;
              input_image[x][y][1] = Mcd_color.g;
              input_image[x][y][2] = Mcd_color.b;
              break;

            default:
              input_image[x][y][0] = Red_color.r;
              input_image[x][y][1] = Red_color.g;
              input_image[x][y][2] = Red_color.b;
              break;
            }
          }
        }
      }
    }
  }
}
